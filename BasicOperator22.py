#BasicOperator22.py
a = int(input())
Total = int(input())
Total += a # Using += Operator
print("The Value of the Total after using += Operator is:", Total)
Total-=a # Using -= Operator
print("The Value of the Total after using -= Operator is:", Total)
Total*=a # Using *= Operator
print("The Value of the Total after using *= Operator is:", Total)
Total//=a # Using //= Operator
print("The Value of the Total after using //= Operator is:", Total)
Total**=a # Using **= Operator
print("The Value of the Total after using **= Operator is:", Total)
Total/=a # Using /= Operator
print("The Value of the Total after using /= Operator is:", Total)
Total%=a # Using %= Operator
print("The Value of the Total after using %= Operator is:", Total)